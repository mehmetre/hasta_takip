class CountryPicker extends StatefulWidget {
  const CountryPicker({super.key});

  @override
  State<CountryPicker> createState() => _CountryPickerState();
}

class _CountryPickerState extends State<CountryPicker> {
  final TextEditingController _controller = TextEditingController();
  List<Map<String, String>> _filteredCountryList = [];
  String _selectedDialCode = '';
  String _selectedFlag = '';
  String _selectedMask = '(000) 000-00-00';

  late MaskTextInputFormatter _maskFormatter;

  @override
  void initState() {
    super.initState();
    _controller.addListener(() => setState(() {}));
    _filteredCountryList = _countryList;
    _maskFormatter = MaskTextInputFormatter(
      mask: _selectedMask,
      filter: {'0': RegExp('[0-9]')},
    );
  }

  @override
  Widget build(BuildContext context) {
    return DraggableScrollableSheet(
      initialChildSize: 1,
      minChildSize: .99,
      builder: (context, controller) {
        return SafeArea(
          child: Scaffold(
            appBar: AppBar(
              toolbarHeight: 64.h,
              forceMaterialTransparency: true,
              automaticallyImplyLeading: false,
              title: TextField(
                style: context.textTheme.footnoteRegular?.copyWith(
                  color: context.color.labelLightPrimary,
                ),
                onTapOutside: onTapOutside(context),
                controller: _controller,
                textAlignVertical: TextAlignVertical.center,
                decoration: InputDecoration(
                  alignLabelWithHint: true,
                  contentPadding: EdgeInsets.zero,
                  constraints: BoxConstraints(maxHeight: 36.h),
                  hintText: 'Country',
                  hintStyle: context.textTheme.footnoteRegular?.copyWith(
                    color: context.color.labelLightTeartly,
                  ),
                  prefixIconConstraints:
                      BoxConstraints.tight(const Size.fromWidth(36)),
                  prefixIcon: Padding(
                    padding: const EdgeInsets.only(left: 4),
                    child: Icon(
                      NyIcons.search,
                      color: context.color.labelLightTeartly,
                      size: 16,
                    ),
                  ),
                  suffixIconConstraints: BoxConstraints.tight(
                    const Size.fromWidth(40),
                  ),
                  suffixIcon: _controller.text.isNotEmpty
                      ? IconButton(
                          padding: EdgeInsets.zero,
                          onPressed: () {
                            _controller.clear();
                            setState(() => _filteredCountryList = _countryList);
                          },
                          icon: const Icon(
                            NyIcons.cross_filled,
                            color: Color(0x571A282F),
                            size: 20,
                          ),
                        )
                      : null,
                ),
                onChanged: (value) {
                  setState(
                    () => _filteredCountryList = _countryList
                        .where(
                          (element) => element['name']!
                              .toLowerCase()
                              .contains(value.toLowerCase()),
                        )
                        .toList(),
                  );
                },
              ),
              actions: [
                Padding(
                  padding: const EdgeInsets.only(right: 8),
                  child: Button.plainAppBar(
                    onPressed: () {
                      context.pop(
                        _selectedDialCode.isEmpty || _selectedFlag.isEmpty
                            ? [
                                '+1',
                                '(000) 000-00-00',
                                MaskTextInputFormatter(
                                  mask: '(000) 000-00-00',
                                  filter: {'0': RegExp('[0-9]')},
                                ),
                                'ðŸ‡ºðŸ‡¸',
                              ]
                            : [
                                _selectedDialCode,
                                _selectedMask,
                                _maskFormatter,
                                _selectedFlag,
                              ],
                      );
                    },
                    title: 'Cancel',
                  ),
                ),
                8.horizontalSpace,
              ],
            ),
            body: Card(
              margin: AppPadding.defaultColumnPadding,
              color: Colors.white,
              surfaceTintColor: Colors.white,
              child: ListView.builder(
                shrinkWrap: true,
                controller: controller,
                itemCount: _filteredCountryList.length,
                itemBuilder: (context, index) {
                  return ListTile(
                    onTap: () {
                      _selectedDialCode =
                          _filteredCountryList[index]['dial_code']!;
                      _selectedFlag = _filteredCountryList[index]['flag']!;
                      _selectedMask = _filteredCountryList[index]['mask'] ??
                          '(000) 0-0-0-0';
                      _maskFormatter = MaskTextInputFormatter(
                        mask: _selectedMask,
                        filter: {'0': RegExp('[0-9]')},
                      );

                      context.pop([
                        _selectedDialCode,
                        _selectedMask,
                        _maskFormatter,
                        _selectedFlag,
                      ]);
                    },
                    leading: SizedBox.square(
                      dimension: 40.r,
                      child: ClipOval(
                        child: Text(
                          _filteredCountryList[index]['flag']!,
                          textAlign: TextAlign.left,
                          style: TextStyle(
                            fontSize: 72.sp,
                            height: .5.h,
                            letterSpacing: -12.w,
                          ),
                        ),
                      ),
                    ),
                    title: Text(_filteredCountryList[index]['name']!),
                    subtitle: Text(_filteredCountryList[index]['dial_code']!),
                  );
                },
              ),
            ),
          ),
        );
      },
    );
  }
}

